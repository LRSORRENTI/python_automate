# In python an expression is a value + operator
# (+ - / *) and they always reduce to a single value
print(2 * 3);

print(5 - 3);

print(3 * 7);

print(22 / 7); # 3.142857142857143

print(2 + 3 * 6); # 20

# In Python order of operations can be overridden 
# using parentheses

print((2 + 3) * 6) # 30

print((5 - 1) * (7 + 1) / (3 - 1)) # 16.0